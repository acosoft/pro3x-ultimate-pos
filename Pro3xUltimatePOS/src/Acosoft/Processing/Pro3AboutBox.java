    private void PromjeniPrikazLicence()
    {
        Pro3App.ProvjeriPotpisLicence();
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");

        if(Pro3App.isLicenced() == true)
        {
            LicenceInfo info = Pro3App.getLicence();
            regNaziv.setText(info.getCompany());
            regEmail.setText(info.getEmail());
            regPeriod.setText(sdf.format(info.getValidfrom()) + " - "
                    + sdf.format(info.getValidto()));
        }
        else Tasks.showMessage("Neuspje≈°na provjera registracije programa.");
    }

