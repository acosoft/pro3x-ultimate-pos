    @Action
    public void IzborLicence()
    {
        JFileChooser jf = new CustomFileChooser();
        jf.setMultiSelectionEnabled(false);
        
        Point pozicija = this.getLocationOnScreen();
        jf.setDialogTitle("Izbor registracijske datoteke");
        jf.setLocation((int)pozicija.getX() + 100, (int)pozicija.getY() + 100);

        jf.setFileFilter(new FileFilter()
        {
            @Override
            public boolean accept(File f)
            {
                return f.isDirectory() || f.getName().endsWith(".xml");
            }

            @Override
            public String getDescription() {
                return "Datoteke sa registracijskim kljuƒçem";
            }
        });

        if(jf.showOpenDialog(this) == JFileChooser.APPROVE_OPTION)
        {
            File newLicence = jf.getSelectedFile();
            File licence = new File(Pro3App.getLicencePath());

            if(licence.exists()) licence.delete();
            newLicence.renameTo(licence);

            PromjeniPrikazLicence();
            
            Tasks.showMessage("Registracijski kljuƒç je uspje≈°no premje≈°ten na predviƒëenu lokaciju.");
        }
    }

    private void appHomepageLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_appHomepageLabelMouseClicked
        Pro3App app = Pro3App.getApplication();
        app.getContext().getTaskService().execute(new Task(app)
        {
            @Override
            protected Object doInBackground() throws Exception 
            {
                setMessage("Otvaram Pro3x Community web stranice.");
                if (java.awt.Desktop.isDesktopSupported()) {
                    java.awt.Desktop.getDesktop().browse(new URI("http://www.mali-zeleni.hr"));
                }
                return null;
            }
        });    
    }//GEN-LAST:event_appHomepageLabelMouseClicked

