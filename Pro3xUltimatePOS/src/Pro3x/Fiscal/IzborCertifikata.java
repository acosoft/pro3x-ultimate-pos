    @Action
    public void IzborPutanje() {
        JFileChooser xf = new JFileChooser();
        
        xf.setFileFilter(new javax.swing.filechooser.FileFilter() {

            @Override
            public boolean accept(File f) {
                return f.isDirectory() || f.getName().endsWith(".pfx");
            }

            @Override
            public String getDescription() {
                return "Sigurnosni ključevi i certifikati ( *.pfx )";
            }
        });
        
        if(xf.showDialog(this.getRootPane(), "Odaberi") == JFileChooser.APPROVE_OPTION)
        {
            putanja.setText(xf.getSelectedFile().getAbsolutePath());
        }
    }
    
