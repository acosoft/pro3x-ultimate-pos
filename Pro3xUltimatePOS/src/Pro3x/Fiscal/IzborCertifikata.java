    @Action
    public void Spremi() {
        try {
            KeyStore ks = KeyStore.getInstance("PKCS12");
            ks.load(new FileInputStream(putanja.getText()), zaporka.getPassword());

            KeyInfo info = new KeyInfo();
            info.setPassword(zaporka.getPassword());
            info.setPath(putanja.getText());
            info.setFiskalizacijaAktivirana(ukljuci.isSelected());
            
            Pro3App.getApplication().getContext().getLocalStorage().save(info, FINA_XML);
            
            Tasks.showMessage("Detalji o certifikatu su spremljeni. Ako premjestite datoteku, račune više neće biti moguće potpisati.");
            
        } catch (Exception ex) {
            Logger.getLogger(IzborCertifikata.class.getName()).log(Level.SEVERE, null, ex);
            Tasks.showMessage("Ne mogu učitati datoteku sa sigurnosnim certifikatima");
        }
    }

