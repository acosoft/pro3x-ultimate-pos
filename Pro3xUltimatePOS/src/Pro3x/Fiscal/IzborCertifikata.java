    public static KeyInfo loadKeyInfo()
    {
        try {
            LocalStorage storage = Pro3App.getApplication().getContext().getLocalStorage();
            KeyInfo key = (KeyInfo) storage.load(FINA_XML);
            if(key != null) return key;
        } catch (Exception ex) {
            Logger.getLogger(IzborCertifikata.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return new KeyInfo();
    }

