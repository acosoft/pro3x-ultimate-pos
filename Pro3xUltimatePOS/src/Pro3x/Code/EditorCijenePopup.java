    @Action
    public void Zatvori()
    {
        editor.stopCellEditing();
        
        JPopupMenu pop = (JPopupMenu) getParent();
        pop.setVisible(false);
    }

    @Action
    public void Spremi()
    {
        editor.setCijena(novaCijena);
        Zatvori();
    }
