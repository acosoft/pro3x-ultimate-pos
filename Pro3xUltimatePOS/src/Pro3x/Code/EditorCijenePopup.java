    private void iznosPopustaFocusLost(java.awt.event.FocusEvent evt)//GEN-FIRST:event_iznosPopustaFocusLost
    {//GEN-HEADEREND:event_iznosPopustaFocusLost
        Double vrijednost = Formatiraj(iznosPopusta);
        novaCijena = staraCijena - vrijednost;

        PromjeniCijenu();
        popust.setText(nf.format(vrijednost / (staraCijena / 100)));
    }//GEN-LAST:event_iznosPopustaFocusLost

    private void popustFocusGained(java.awt.event.FocusEvent evt)//GEN-FIRST:event_popustFocusGained
    {//GEN-HEADEREND:event_popustFocusGained
        popust.selectAll();
    }//GEN-LAST:event_popustFocusGained

    private void popustFocusLost(java.awt.event.FocusEvent evt)//GEN-FIRST:event_popustFocusLost
    {//GEN-HEADEREND:event_popustFocusLost
        Double popustPostotak = Formatiraj(popust);
        Double iznos = staraCijena * (popustPostotak / 100);
        novaCijena = staraCijena - iznos;

        PromjeniCijenu();
        iznosPopusta.setText(nf.format(iznos));
    }//GEN-LAST:event_popustFocusLost

    private void PromjeniCijenu()
    {
        String prikaz = nf.format(novaCijena);
        prikazCijene.setText(prikaz);
    }

    @Override
    public boolean requestFocusInWindow()
    {
        grabFocus();
        return popust.requestFocusInWindow();
    }

    private Double Formatiraj(JTextField polje)
    {
        try
        {
            Double vrijednost = nf.parse(polje.getText()).doubleValue();
            polje.setText(nf.format(vrijednost));

            return vrijednost;
        }
        catch (ParseException ex)
        {
            Logger.getLogger(EditorCijenePopup.class.getName()).log(Level.SEVERE, null, ex);
            Tasks.showMessage("Iznimka pri formatiranju prikaza. Provjerite upisane podatke.");

            return 0D;
        }
    }

